# 导入整个模块：使用import语句导入了名为module_name.py的整个模块，就可使用下面的语法来使用其中任何一个函数:module_name.function_name()

# 导入模块中的特定函数：from module_name import function_name1,function_name2...

# 给函数指定别名：from module_name import function_name as fn

# 给模块指定别名：import module_name as mn

# 导入模块中的所有函数：from module_name import *（不推荐使用）
